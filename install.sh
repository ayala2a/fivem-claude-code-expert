#!/usr/bin/env bash
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
CLAUDE_DIR="$HOME/.claude"
CLAUDE_MD="$CLAUDE_DIR/CLAUDE.md"

echo "============================================"
echo "  FiveM Claude Code Expert — Installation"
echo "============================================"
echo ""

# 1. Clone refs
echo "==> Etape 1/3 : Installation des refs..."
"$SCRIPT_DIR/setup.sh"
echo ""

# 2. Install skills
echo "==> Etape 2/3 : Installation des skills Claude Code..."
echo ""

if command -v bunx &>/dev/null; then
    echo "[skill] fivem-natives-skill (via bunx)..."
    bunx skills add heyyczer/fivem-natives-skill 2>/dev/null || echo "  WARN: echec install fivem-natives-skill (pas grave, peut etre installe manuellement)"
else
    echo "[skip] bunx non trouve — installer Bun pour fivem-natives-skill"
    echo "       https://bun.sh ou: curl -fsSL https://bun.sh/install | bash"
fi

if command -v npx &>/dev/null; then
    echo "[skill] fiveai-skills (via npx)..."
    npx skills add germanfndez/fiveai-skills 2>/dev/null || echo "  WARN: echec install fiveai-skills (pas grave, peut etre installe manuellement)"
else
    echo "[skip] npx non trouve — installer Node.js pour fiveai-skills"
fi

echo ""

# 3. Setup CLAUDE.md
echo "==> Etape 3/3 : Configuration de Claude Code..."

mkdir -p "$CLAUDE_DIR"

REFS_PATH="$SCRIPT_DIR/refs"
MARKER="# FiveM Expert (auto-generated by fivem-claude-code-expert)"

if [ -f "$CLAUDE_MD" ]; then
    if grep -q "$MARKER" "$CLAUDE_MD" 2>/dev/null; then
        echo "[skip] CLAUDE.md deja configure (marqueur present)"
    else
        echo "[append] Ajout de la config FiveM dans $CLAUDE_MD..."
        cat >> "$CLAUDE_MD" << EOF

---

$MARKER
# Pour la configuration FiveM complete, voir :
# $SCRIPT_DIR/CLAUDE.md

# Refs locales (sources reelles) :
# $REFS_PATH/
#   esx_core/    qb-core/     qbx_core/    ox_core/
#   ox_lib/      ox_inventory/ ox_target/   ox_doorlock/
#   ox_fuel/     ox_mdt/      npwd/        pma-voice/

# Regle : Quand tu ne connais pas une API FiveM, lis le code source dans
# $REFS_PATH/ avant de repondre. Ne jamais inventer un native ou une API.
EOF
    fi
else
    echo "[create] Creation de $CLAUDE_MD..."
    cat > "$CLAUDE_MD" << EOF
$MARKER
# Pour la configuration FiveM complete, voir :
# $SCRIPT_DIR/CLAUDE.md

# Refs locales (sources reelles) :
# $REFS_PATH/
#   esx_core/    qb-core/     qbx_core/    ox_core/
#   ox_lib/      ox_inventory/ ox_target/   ox_doorlock/
#   ox_fuel/     ox_mdt/      npwd/        pma-voice/

# Regle : Quand tu ne connais pas une API FiveM, lis le code source dans
# $REFS_PATH/ avant de repondre. Ne jamais inventer un native ou une API.
EOF
fi

echo ""
echo "============================================"
echo "  Installation terminee !"
echo "============================================"
echo ""
echo "Utilisation :"
echo "  1. Ouvre Claude Code dans ton projet FiveM"
echo "  2. Copie CLAUDE.md dans ton projet : cp $SCRIPT_DIR/CLAUDE.md /ton/projet/.claude/CLAUDE.md"
echo "     OU ajoute le chemin dans ton .claude/settings.json"
echo "  3. Claude est maintenant expert FiveM"
echo ""
echo "Mise a jour des refs : ./update.sh"
echo ""
